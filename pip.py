# Third party packages - pip: the standard package manager for Python
# The list of available packages can be found at pypi.org

# `pip install <package name>`
# Installs the package globally

# `pip show <package name>`
# Shows info about the package

# `pip install -U <package name>`
# Installs latest version of that package

# `pip uninstall <package name>`
# Uninstalls package

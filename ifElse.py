# Control Flow - `if`, `else` `elif` statements
condition = False
name = "Wyatt"

if condition == True:
    print("The condition")
    print("was True")
elif name == "Richard":
    print("Hello Richard!")
elif name == "Wyatt":
    print("Hello Wyatt!!")  # Hello Wyatt!!
elif name == "Henry":
    print("Hello Henry!")
else:
    print("The condition")
    print("was False")
